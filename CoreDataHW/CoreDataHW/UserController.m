#import "UserController.h"
#import "DataManager.h"

@interface UserController ()

@property (strong,nonatomic) NSArray *users;

@end

@implementation UserController

- (void)viewDidLoad {
    [super viewDidLoad];
    
    self.users = [[DataManager sharedManager] getUsersArray];
}

#pragma mark - Table view data source

- (NSInteger)numberOfSectionsInTableView:(UITableView *)tableView {

    return 1;
}

- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section {
    
    return [self.users count];
}


- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath {
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:@"UserCell" forIndexPath:indexPath];
    
    NSManagedObject *user = [self.users objectAtIndex:indexPath.row];
    NSString *nameString = [NSString stringWithFormat:@"%@ %@",[user valueForKey:@"firstName"],[user valueForKey:@"lastName"]];
    cell.textLabel.text = nameString;
    cell.detailTextLabel.text = [NSString stringWithFormat:@"%@ %.0u ",[user valueForKey:@"adress"],arc4random_uniform(100)];
    return cell;
}


